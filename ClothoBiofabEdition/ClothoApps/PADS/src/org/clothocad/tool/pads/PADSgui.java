    // updates the table with simulation check information
    private void updateTable(InvertSim simulation)
    {
        // remove everything from jPanel6
        jPanel6.removeAll();

        // update this change
        jPanel6.updateUI();

        // define the column names
        String col[] = {"Permutation","Actual Config","Input","Input Size"};

        // create a new table for permutationCheck
        JTable jTable2 = new JTable(simulation.permutationCheck,col);

        // set the header color
        jTable2.getTableHeader().setBackground(Color.yellow);

        // stick the table in a scroll pane
        JScrollPane pane = new JScrollPane(jTable2);

        // give the panel a layout
        jPanel6.setLayout(new BorderLayout());

        // stick the pane in the panel
        jPanel6.add(pane,BorderLayout.CENTER);

        // new status label
        JLabel completePermSet = new JLabel();
        completePermSet.setVisible(true);

        // new panel to stick it in
        //JPanel permPanel = new JPanel();

        pane.setPreferredSize(new Dimension(250,370) );
        jPanel7.setVisible(true);
        jLabel7.setLayout(new BorderLayout());
        jLabel9.setAlignmentX(CENTER_ALIGNMENT);
        // add the label to the panel
        //jPanel7.add(completePermSet,BorderLayout.CENTER);

        // add this panel to the bottom of our jpanel
        //jPanel6.add(permPanel,BorderLayout.PAGE_END);
        jPanel6.add(jPanel7,BorderLayout.PAGE_END);

        // run a check for complete permutation set
        boolean complete = simulation.isCompleteSet();

        // update the label based on completion
        if (complete)
        {
            // set the panel color and the label text
            jPanel7.setBackground(Color.green);
            jLabel9.setText("COMPLETE SET OF PERMUTATIONS!");
            jPanel7.repaint();

        }
        else
        {
            // set the panel color and the label text
            jPanel7.setBackground(Color.red);
            jLabel9.setText("Incomplete set of permutations.  "
                    +simulation.getRemaining()+" permutations were not hit.");
            jPanel7.repaint();
        }
    }


