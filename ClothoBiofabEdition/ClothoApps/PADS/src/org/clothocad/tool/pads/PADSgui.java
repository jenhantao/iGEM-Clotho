        // explain starting configuration and inputs
        outText.append("Starting Design: "+simulation.designString()+"\n");
        outText.append("Starting Part Permutation: "+simulation.partsString()+
                "\n\n");

        // iterate through the input array and make switches

        for (int i = 0; i < simulation.inputArray.length;i++){

            if (simulation.isPossible(simulation.inputArray[i])) {
                // now that we know it is possible, we need to make the inversion

                // tell them what you're adding
                outText.append("Add invertase "+simulation.inputArray[i]+" using sites "+
                        simulation.currentSites[0]+" and "+simulation.currentSites[1]+"\n");

                // show what the output was before
                outText.append("    Before: "+simulation.designString()+"\n");

                // store the parts string before in temporary, just for displaying
                before = simulation.partsString();

                // now invert
                simulation.invert();

                // output new design
                outText.append("    After: "+simulation.designString()+"\n");
                outText.append("    Permutation before: "+before+"\n");
                outText.append("    Permutation after: "+simulation.partsString()+"\n\n");

            } else
                // output that it is not possible
                outText.append("Invertase "+simulation.inputArray[i]+" will not work.\n\n");
        }

        // show the user the final structure
        outText.append("Final structure: "+simulation.designString()+"\n");
        outText.append("Final permutation: "+simulation.partsString()+"\n\n");
        outText.append("------------------------------------------------"
                + "-------------------------------------\n\n");
