    private void saveBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveBtnActionPerformed
        short quality = (short) QualityCombo.getSelectedIndex();
        _sample.changeQuality(quality);
        _sample.changeName(sampleNameField.getText());
        Person author = Person.retrieveByName(authorNameField.getText());
        if (author != null) {
            _sample.changeAuthor(author);
        }

        try {
            String svol = volField.getText();
            Double adub = Double.parseDouble(svol);
            _sample.changeVolume(adub);
        } catch (Exception e) {
        }

        Plasmid plasmid = Plasmid.retrieveByName(plasmidField.getText());
        if (plasmid != null) {
            _sample.changePlasmid(plasmid);
        }

        Strain strain = Strain.retrieveByName(strainField.getText());
        if (strain != null) {
            _sample.changeSourceStrain(strain);
        }

        setData();
        _sample.saveDefault();
    }//GEN-LAST:event_saveBtnActionPerformed

