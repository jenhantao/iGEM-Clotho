    private void setData() {
        int quality = _sample.getQuality();
        QualityCombo.setSelectedIndex(quality);

        sampleNameField.setText((_sample.getName()));
        authorNameField.setText(_sample.getAuthor().getName());
        volField.setText(Double.toString(_sample.getVolume()));

        Plasmid plasmid = _sample.getPlasmid();
        Strain strain = _sample.getSourceStrain();

        strainField.setText(strain.getName());
        plasmidField.setText(plasmid.getName());
        formatField.setText(plasmid.getFormat().getName());

        plateField.setText(_sample.getPlate().toString());
        wellField.setText(_sample.getContainer().getWell());
    }

